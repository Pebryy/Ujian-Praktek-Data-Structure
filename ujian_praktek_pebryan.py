## Soal Pratikum No.3 - Sistem Manajemen Playlist Musik

class Node:
    def __init__(self, judul, penyanyi):
        self.judul = judul
        self.penyanyi = penyanyi

        self.next = None
        self.prev = None


class CircularDoublyLinkedList:

    def __init__(self):
        self.head = None
        self.current = None

    def tambah_lagu(self, judul, penyanyi):

        baru = Node(judul, penyanyi)

        if self.head is None:

            self.head = baru

            baru.next = baru
            baru.prev = baru

            self.current = baru

        else:

            tail = self.head.prev

            tail.next = baru

            baru.prev = tail

            baru.next = self.head

            self.head.prev = baru

    def hapus_lagu(self, judul):

        if self.head is None:
            print("Playlist kosong")
            return

        temp = self.head

        while True:

            if temp.judul.lower() == judul.lower():

                if temp.next == temp:

                    self.head = None
                    self.current = None

                else:

                    temp.prev.next = temp.next
                    temp.next.prev = temp.prev

                    if temp == self.head:
                        self.head = temp.next

                    if temp == self.current:
                        self.current = temp.next

                print("Lagu dihapus")
                return

            temp = temp.next

            if temp == self.head:
                break

        print("Lagu tidak ditemukan")

    def tampilkan(self):

        if self.head is None:
            print("Playlist kosong")
            return

        temp = self.head
        no = 1

        print("\nPlaylist:")

        while True:

            print(
                f"{no}. {temp.judul} - {temp.penyanyi}"
            )

            temp = temp.next

            no += 1

            if temp == self.head:
                break

    def next_lagu(self):

        if self.current:
            self.current = self.current.next

            print(
                "Now Playing:",
                self.current.judul,
                "-",
                self.current.penyanyi
            )

    def prev_lagu(self):

        if self.current:
            self.current = self.current.prev

            print(
                "Now Playing:",
                self.current.judul,
                "-",
                self.current.penyanyi
            )

    def cari(self, judul):

        if self.head is None:
            return

        temp = self.head

        while True:

            if temp.judul.lower() == judul.lower():

                print(
                    "Ditemukan:",
                    temp.judul,
                    "-",
                    temp.penyanyi
                )

                return

            temp = temp.next

            if temp == self.head:
                break

        print("Lagu tidak ditemukan")


playlist = CircularDoublyLinkedList()

