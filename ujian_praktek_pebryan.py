## Soal Pratikum No.2 - Linked List

class Node:
    def __init__(self, data):
        self.data = data
        self.next = None


class LinkedList:
    def __init__(self):
        self.head = None

    def insert_awal(self, data):
        node_baru = Node(data)

        node_baru.next = self.head
        self.head = node_baru

    def insert_akhir(self, data):
        node_baru = Node(data)

        if self.head is None:
            self.head = node_baru
            return

        temp = self.head

        while temp.next:
            temp = temp.next

        temp.next = node_baru

    def tampil(self):

        if self.head is None:
            print("List kosong")
            return

        temp = self.head

        while temp:
            print(temp.data, end=" -> ")
            temp = temp.next

        print("None")

    def cari(self, key):

        temp = self.head

        while temp:

            if temp.data == key:
                return True

            temp = temp.next

        return False

    def hapus(self, key):

        temp = self.head

        if temp and temp.data == key:
            self.head = temp.next
            return

        prev = None

        while temp and temp.data != key:
            prev = temp
            temp = temp.next

        if temp is None:
            return

        prev.next = temp.next

ll = LinkedList()

